def sabiranje(prviBroj, drugiBroj):
    zbir = prviBroj + drugiBroj
    print('Zbir broja ' + str(prviBroj) + ' i broja ' + str(drugiBroj) + ' je: ' + str(zbir))

def oduzimanje(prviBroj, drugiBroj):
    razlika = prviBroj - drugiBroj
    print('Razlika broja ' + str(prviBroj) + ' i broja ' + str(drugiBroj) + ' je: ' + str(razlika))

def mnozenje(prviBroj, drugiBroj):
    proizvod = prviBroj * drugiBroj
    print('Proizvod broja ' + str(prviBroj) + ' i broja ' + str(drugiBroj) + ' je: ' + str(proizvod))

def deljenje(prviBroj, drugiBroj):
    kolicnik = prviBroj / drugiBroj
    print('Kolicnik broja ' + str(prviBroj) + ' i broja ' + str(drugiBroj) + ' je: ' + str(kolicnik))